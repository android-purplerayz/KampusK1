package epaper;

public class ModelEpaperData {

    String epaper_id;
    String student_id;
    String age_id;
    String subject_id;
    String title;
    String epaper_image;
    String link;
    String file;

    public String getEpaper_id() {
        return epaper_id;
    }

    public void setEpaper_id(String epaper_id) {
        this.epaper_id = epaper_id;
    }

    public String getStudent_id() {
        return student_id;
    }

    public void setStudent_id(String student_id) {
        this.student_id = student_id;
    }

    public String getAge_id() {
        return age_id;
    }

    public void setAge_id(String age_id) {
        this.age_id = age_id;
    }

    public String getSubject_id() {
        return subject_id;
    }

    public void setSubject_id(String subject_id) {
        this.subject_id = subject_id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getEpaper_image() {
        return epaper_image;
    }

    public void setEpaper_image(String epaper_image) {
        this.epaper_image = epaper_image;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }
}
